			var literals = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
			var chars = " !\"#$%&'()*+,-./";
			Enumerable.Range(0, 100).ToList().ForEach(x => Code.Add($"{x}".PadLeft(2, '0'), x));
			Enumerable.Range(16, 10).ToList().ForEach(x => Code.Add($"{x - 16}", x));
			Enumerable.Range(33, 26).ToList().ForEach(x => Code.Add($"{literals[x - 33]}", x));
			Enumerable.Range(65, 26).ToList().ForEach(x => Code.Add($"{literals.ToLower()[x - 65]}", x));
			Enumerable.Range(0, 16).ToList().ForEach(x => Code.Add($"{chars[x]}", x));
